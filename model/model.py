class ConstellationResnet(BaseModel):
    def __init__(self, num_classes=10, input_channels=3) -> None:
        super().__init__()
        self._prepare_base_model(num_class = num_classes, base_model = 'resnet18', input_channels=input_channels)
    
    def _prepare_base_model(self, base_model, num_class, input_channels=3):
        if 'resnet' in base_model:
            # 使用getattr方便更换模型
            self.model = getattr(models, base_model)(pretrained=True)
            feature_dim = getattr(self.model, 'fc').in_features
            setattr(self.model, 'fc', nn.Linear(feature_dim, num_class))
            if input_channels != 3:
                self.model.conv1 = nn.Conv2d(input_channels, 64, kernel_size=7, stride=2, padding=3, bias=False)
        else:
            raise ValueError('Unknown base model: {}'.format(base_model))
    
    def forward(self, x):
        out = self.model(x)
        return out

